-- 月別教室データ
CREATE TABLE monthly_data (
  id INT AUTO_INCREMENT PRIMARY KEY,
  month VARCHAR(10) NOT NULL COMMENT '月（例: 4月, 1月）',
  account VARCHAR(50) NOT NULL COMMENT '計上先（教室名）',
  cost INT DEFAULT 0 COMMENT '費用',
  top_display INT DEFAULT 0 COMMENT '上位表示',
  remarketing INT DEFAULT 0 COMMENT 'リマーケティング',
  other_cost INT DEFAULT 0 COMMENT 'その他',
  yahoo INT DEFAULT 0 COMMENT 'Yahoo広告',
  sort_order INT DEFAULT 0 COMMENT '表示順',
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY unique_month_account (month, account)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

