-- プロジェクト（教室）テーブル
CREATE TABLE IF NOT EXISTS projects (
  id VARCHAR(64) PRIMARY KEY,
  name VARCHAR(200) NOT NULL COMMENT '教室名',
  open_date DATE NOT NULL COMMENT 'オープン予定日',
  store_available_date DATE DEFAULT NULL COMMENT '店舗入場可能日',
  phone_number_date DATE DEFAULT NULL COMMENT '電話番号発番日',
  staff JSON DEFAULT NULL COMMENT '担当者リスト',
  status ENUM('準備中','オープン済','アーカイブ') DEFAULT '準備中',
  version INT DEFAULT 1,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_status (status),
  INDEX idx_open_date (open_date)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

-- タスクテーブル
CREATE TABLE IF NOT EXISTS tasks (
  id VARCHAR(64) PRIMARY KEY,
  project_id VARCHAR(64) NOT NULL,
  category VARCHAR(100) NOT NULL COMMENT 'カテゴリ名',
  name VARCHAR(300) NOT NULL COMMENT 'タスク名',
  start_date DATE NOT NULL,
  end_date DATE NOT NULL,
  duration INT NOT NULL DEFAULT 1,
  status ENUM('未着手','進行中','完了','遅延') DEFAULT '未着手',
  assignee VARCHAR(100) DEFAULT '' COMMENT '担当者',
  note TEXT DEFAULT NULL COMMENT '備考',
  sort_order INT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_project (project_id),
  FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

-- 備品テーブル
CREATE TABLE IF NOT EXISTS equipment (
  id VARCHAR(64) PRIMARY KEY,
  project_id VARCHAR(64) NOT NULL,
  category VARCHAR(100) NOT NULL COMMENT 'カテゴリ名',
  name VARCHAR(300) NOT NULL COMMENT '品名',
  qty VARCHAR(50) DEFAULT '' COMMENT '数量',
  source VARCHAR(100) DEFAULT '' COMMENT '購入先',
  assignee VARCHAR(100) DEFAULT '' COMMENT '担当',
  checked TINYINT(1) DEFAULT 0 COMMENT '準備済',
  arrived TINYINT(1) DEFAULT 0 COMMENT '到着済',
  note TEXT DEFAULT NULL COMMENT '備考',
  sort_order INT DEFAULT 0,
  created_at DATETIME DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  INDEX idx_project (project_id),
  FOREIGN KEY (project_id) REFERENCES projects(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

-- タスクテンプレート
CREATE TABLE IF NOT EXISTS task_templates (
  id INT AUTO_INCREMENT PRIMARY KEY,
  template_name VARCHAR(200) DEFAULT 'デフォルト',
  category VARCHAR(100) NOT NULL,
  name VARCHAR(300) NOT NULL,
  offset_days INT NOT NULL COMMENT 'オープン日からの日数',
  duration INT NOT NULL DEFAULT 1,
  note TEXT DEFAULT NULL,
  sort_order INT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

-- 備品テンプレート
CREATE TABLE IF NOT EXISTS equip_templates (
  id INT AUTO_INCREMENT PRIMARY KEY,
  template_name VARCHAR(200) DEFAULT 'デフォルト',
  category VARCHAR(100) NOT NULL,
  name VARCHAR(300) NOT NULL,
  qty VARCHAR(50) DEFAULT '',
  source VARCHAR(100) DEFAULT '',
  assignee VARCHAR(100) DEFAULT '',
  sort_order INT DEFAULT 0
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_general_ci;

